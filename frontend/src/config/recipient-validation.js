export const BLOCK_CHECK_CONFIG = {
  TIMEOUT_MS: 10000,
  RETRY_ATTEMPTS: 3,
  RETRY_DELAY_MS: 1000,
  CACHE_TTL_MS: 60000,
};

export const RECIPIENT_VALIDATION_CONFIG = {
  MIN_ADDRESS_LENGTH: 34,
  MAX_ADDRESS_LENGTH: 42,
  CONTRACT_SEPARATOR: '.',
  VALID_PREFIXES: ['SP', 'SM', 'ST'],
};

export const UI_FEEDBACK_CONFIG = {
  ERROR_DISPLAY_DURATION_MS: 5000,
  BLOCK_CHECK_DEBOUNCE_MS: 500,
  VALIDATION_MESSAGE_MAX_LENGTH: 200,
};

export const ANALYTICS_CONFIG = {
  ENABLE_TRACKING: true,
  TRACK_FAILED_CHECKS: true,
  SAMPLE_RATE: 1.0,
  BATCH_SIZE: 10,
  FLUSH_INTERVAL_MS: 30000,
};

export const ERROR_RECOVERY_CONFIG = {
  ALLOW_RETRY: true,
  AUTO_RETRY_FAILED_CHECKS: false,
  SHOW_RETRY_BUTTON: true,
  RETRY_BUTTON_DELAY_MS: 2000,
};

export function isBlockCheckConfigValid() {
  return (
    BLOCK_CHECK_CONFIG.TIMEOUT_MS > 0 &&
    BLOCK_CHECK_CONFIG.RETRY_ATTEMPTS >= 0 &&
    BLOCK_CHECK_CONFIG.RETRY_DELAY_MS >= 0 &&
    BLOCK_CHECK_CONFIG.CACHE_TTL_MS > 0
  );
}

export function isValidationConfigValid() {
  return (
    RECIPIENT_VALIDATION_CONFIG.MIN_ADDRESS_LENGTH > 0 &&
    RECIPIENT_VALIDATION_CONFIG.MAX_ADDRESS_LENGTH >= RECIPIENT_VALIDATION_CONFIG.MIN_ADDRESS_LENGTH &&
    RECIPIENT_VALIDATION_CONFIG.VALID_PREFIXES.length > 0
  );
}

export function getEffectiveTimeout() {
  const baseTimeout = BLOCK_CHECK_CONFIG.TIMEOUT_MS;
  const retryOverhead = BLOCK_CHECK_CONFIG.RETRY_ATTEMPTS * BLOCK_CHECK_CONFIG.RETRY_DELAY_MS;
  return baseTimeout + retryOverhead;
}

export function shouldTrackEvent(eventType) {
  if (!ANALYTICS_CONFIG.ENABLE_TRACKING) return false;
  if (eventType === 'FAILED_CHECK' && !ANALYTICS_CONFIG.TRACK_FAILED_CHECKS) return false;
  return Math.random() <= ANALYTICS_CONFIG.SAMPLE_RATE;
}
